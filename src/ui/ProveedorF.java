    private void TablaProveedorMouseClicked(java.awt.event.MouseEvent evt) {
        int filaSeleccionada = TablaProveedor.getSelectedRow();

        if (filaSeleccionada >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();

            txtIdProveedor.setText(modelo.getValueAt(filaSeleccionada, 0).toString());
            txtNombreP.setText(modelo.getValueAt(filaSeleccionada, 1).toString());
            txtEmpresa.setText(modelo.getValueAt(filaSeleccionada, 2).toString());
            txtTelefono.setText(modelo.getValueAt(filaSeleccionada, 3).toString());
            txtPrecioMinimo.setText(modelo.getValueAt(filaSeleccionada, 4).toString());
            txtPlazoEntrega.setText(modelo.getValueAt(filaSeleccionada, 5).toString());
            txtContacto.setText(modelo.getValueAt(filaSeleccionada, 6).toString());
        }
    }

