    public void listarProveedores() {
        DefaultTableModel modelo = (DefaultTableModel) TablaProveedor.getModel();
        modelo.setRowCount(0); // Limpiar filas antes de cargar

        List<Proveedor> lista = dao.listarProveedores();

        for (Proveedor p : lista) {
            modelo.addRow(new Object[]{
                p.getIdProveedor(),
                p.getNombre(),
                p.getEmpresa(),
                p.getTelefono(),
                  p.getPrecioMinimo(),
                p.getPlazoEntrega(),
                p.getContacto()
            });
        }
    }

