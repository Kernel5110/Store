    private void TablaAlmacenMouseClicked(java.awt.event.MouseEvent evt) {                                          
        // Obtener la fila seleccionada
        int filaSeleccionada = TablaAlmacen.getSelectedRow();
        
        if (filaSeleccionada >= 0) {
            // Obtener los valores de la fila seleccionada
            DefaultTableModel modelo = (DefaultTableModel) TablaAlmacen.getModel();
            
            // Llenar los campos de texto con los datos de la fila seleccionada
            txtIdAlmacen.setText(modelo.getValueAt(filaSeleccionada, 0).toString());
            txtNombreA.setText(modelo.getValueAt(filaSeleccionada, 1).toString());
            txtUbicacionA.setText(modelo.getValueAt(filaSeleccionada, 2).toString());
        }
    }                                         

