    public void listarAlmacenes() {
        DefaultTableModel modelo = (DefaultTableModel) TablaAlmacen.getModel();
        modelo.setRowCount(0); // Limpiar filas antes de cargar

        AlmacenDAO dao = new AlmacenDAO();
        List<Almacen> lista = dao.listarAlmacenes();

        for (Almacen a : lista) {
            modelo.addRow(new Object[]{
                a.getIdAlmacen(),
                a.getNombre(),
                a.getUbicacion()
            });
        }
    }

